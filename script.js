

let per = Number(prompt("Enter Number"));
if(per>50 && per<60)
{
    document.write("C Grade");
    
}
else if (per>60 && per<=70)  
{
    document.write("B Grade");
}
else if (per>70 && per<=80)
    {
        document.write("A Grade");
    }
    else if (per>80 && per<=100)
        {
            document.write("A1 Grade");
        }
else 
{
    document.write("Fail");
    
}

